var x, y 

function add(x, y) {
  return x + y;
}

function subtract(x, y) {
  return x - y;
}

function multiply(x, y) {
  return x * y;
}

function divide(x, y) {
  return x / y;
}

function inc(n) {
  x = n++
  n = x
  return x
}

function dec(n) {
  return n--
}

function makeInt(n) {
  return n.parseInt ;
}

function preserveDecimal(n) {
  return n.parseFloat;
}


